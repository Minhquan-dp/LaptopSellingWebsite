CREATE TRIGGER TONGHOADON ON CTHD FOR INSERT
AS
	DECLARE @MALAPTOP NVARCHAR(255), @SOLUONG INT, @MAHD NVARCHAR(10),@TONGTIEN MONEY;
	SET @MAHD = (SELECT MAHD FROM INSERTED);
	SET @MALAPTOP = (SELECT MALAPTOP FROM INSERTED);
	SET @SOLUONG = (SELECT SOLUONG FROM INSERTED);
IF(@SOLUONG > 0)
	BEGIN
		SET @TONGTIEN = (SELECT SUM(A.TONGGIA) AS TONGGIA FROM (
							   SELECT CT.MALAPTOP, TT.TENLAPTOP, (CT.SOLUONG * TT.GIABAN) AS TONGGIA 
							   FROM HOADON HD JOIN CTHD CT ON HD.MAHD = CT.MAHD 
							   JOIN THONGTINLAPTOP TT ON CT.MALAPTOP = TT.MALAPTOP
							   WHERE HD.MAHD = @MAHD) AS A)
		UPDATE HOADON SET TRIGIA = @TONGTIEN WHERE MAHD = @MAHD
	END
ELSE
BEGIN
	RAISERROR('KHÔNG THỂ TÍNH GIÁ TRỊ TỔNG TIỀN',10,1);
	ROLLBACK
END

