CREATE TRIGGER UPDATEKHOHANG ON CTHD  FOR INSERT
AS
	DECLARE @MALAPTOP NVARCHAR(255),@SLXUAT INT, @SLNHAP INT;
	SET @MALAPTOP = (SELECT MALAPTOP FROM INSERTED);

IF(@MALAPTOP IN(SELECT DISTINCT MALAPTOP FROM THONGTINLAPTOP))
	BEGIN
		SET @SLNHAP = (SELECT SLNHAP FROM KHOHANG WHERE MALAPTOP = @MALAPTOP);
		SET @SLXUAT = (SELECT SUM(SOLUONG) FROM CTHD WHERE MALAPTOP = @MALAPTOP);
		UPDATE KHOHANG SET SLXUAT = @SLXUAT WHERE MALAPTOP = @MALAPTOP;
		UPDATE KHOHANG SET TONKHO = (@SLNHAP - @SLXUAT) WHERE MALAPTOP = @MALAPTOP;
	END
ELSE
BEGIN
	RAISERROR ('KHÔNG THỂ THÊM CTHD!',10,1)
	ROLLBACK
END

